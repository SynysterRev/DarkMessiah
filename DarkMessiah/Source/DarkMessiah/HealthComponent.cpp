void UHealthComponent::InflictDamage(int32 _damage)
{
	if (!IsDead)
	{
		Health = FMath::Clamp(Health - _damage, 0, MaxHealth);
		if (Health <= 0.0f)
		{
			IsDead = true;
		}
	}
}

